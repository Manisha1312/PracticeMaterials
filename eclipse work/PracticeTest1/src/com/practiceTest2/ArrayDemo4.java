package com.practiceTest2;

public class ArrayDemo4 {
	
	void printArray(int arr1[][]){
		for(int i=0;i<arr1.length;i++){ // count how many arrays (rows)
			for(int j =0;j<arr1[i].length;j++){ // how many elements in each array(column)
				
				System.out.print(arr1[i][j]+" ");
				
			}
			System.out.println();
			
		}
		
			
		}
	

}
